/**
 * @brief  LCD初始化
 * @note   按照ST7735R标准初始化序列进行配置
 */
void ST7735_Init(void)
{
    /*
     * 更通用的最小初始化序列：
     * 用于快速排查“白屏/不出图”的通信问题与屏幕兼容性问题。
     * 若最小序列能点亮并正常填充颜色，再按屏幕版本细调 FRMCTR/PWCTR/Gamma/偏移。
     */

    /* 建议先确保 CS 为高，避免上电期间误选中 */
    HAL_GPIO_WritePin(ST7735_CS_PORT, ST7735_CS_PIN, GPIO_PIN_SET);

    /* 硬件复位 */
    HAL_GPIO_WritePin(ST7735_RES_PORT, ST7735_RES_PIN, GPIO_PIN_RESET);
    ST7735_Delay(20);
    HAL_GPIO_WritePin(ST7735_RES_PORT, ST7735_RES_PIN, GPIO_PIN_SET);
    ST7735_Delay(150);

    /* 软件复位 */
    ST7735_WriteCommand(0x01);
    ST7735_Delay(150);

    /* 退出休眠模式 */
    ST7735_WriteCommand(0x11);
    ST7735_Delay(120);

    /* 颜色模式：RGB565 (16位) */
    ST7735_WriteCommand(0x3A);
    ST7735_WriteData(0x05);

    /*
     * 显示方向（MADCTL）
     * 你当前现象为“文字上下颠倒”，通常是需要 180° 旋转：MY|MX = 0xC0
     */
    ST7735_WriteCommand(0x36);
    ST7735_WriteData(0xC0);

    /* 可选：反显开/关，用于肉眼确认“命令是否生效” */
    ST7735_WriteCommand(0x21); /* INVON */
    ST7735_Delay(10);
    ST7735_WriteCommand(0x20); /* INVOFF */

    /* 开启显示 */
    ST7735_WriteCommand(0x29);
    ST7735_Delay(20);

    /* 开启背光并清屏 */
    ST7735_DisplayOn();
    ST7735_FillColor(ST7735_COLOR_BLACK);
}

/**
 * @brief  LCD软件复位
 */
void ST7735_Reset(void)
{
    HAL_GPIO_WritePin(ST7735_RES_PORT, ST7735_RES_PIN, GPIO_PIN_RESET);
    ST7735_Delay(10);
    HAL_GPIO_WritePin(ST7735_RES_PORT, ST7735_RES_PIN, GPIO_PIN_SET);
    ST7735_Delay(120);
}

/**
 * @brief  发送命令到LCD
 * @param  cmd: 命令字节
 */
void ST7735_WriteCommand(uint8_t cmd)
{
    HAL_GPIO_WritePin(ST7735_DC_PORT, ST7735_DC_PIN, GPIO_PIN_RESET);
    HAL_GPIO_WritePin(ST7735_CS_PORT, ST7735_CS_PIN, GPIO_PIN_RESET);
    HAL_SPI_Transmit(&hspi2, &cmd, 1, 100);
    HAL_GPIO_WritePin(ST7735_CS_PORT, ST7735_CS_PIN, GPIO_PIN_SET);
}

/**
 * @brief  发送数据到LCD
 * @param  data: 数据字节
 */
void ST7735_WriteData(uint8_t data)
{
    HAL_GPIO_WritePin(ST7735_DC_PORT, ST7735_DC_PIN, GPIO_PIN_SET);
    HAL_GPIO_WritePin(ST7735_CS_PORT, ST7735_CS_PIN, GPIO_PIN_RESET);
    HAL_SPI_Transmit(&hspi2, &data, 1, 100);
    HAL_GPIO_WritePin(ST7735_CS_PORT, ST7735_CS_PIN, GPIO_PIN_SET);
}

/**
 * @brief  设置LCD显示窗口
 * @param  x0, y0: 起始坐标
 * @param  x1, y1: 结束坐标
 * @note   添加偏移量(x+2, y+1)以适配1.44寸显示屏
 */
void ST7735_SetAddrWindow(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1)
{
    /* 1.8寸ST7735S偏移量：x+0, y+1 */
    x0 += 0;
    y0 += 1;
    x1 += 0;
    y1 += 1;
    
    /* 设置列地址 */
    ST7735_WriteCommand(0x2A);
    ST7735_WriteData(x0 >> 8);
    ST7735_WriteData(x0 & 0xFF);
    ST7735_WriteData(x1 >> 8);
    ST7735_WriteData(x1 & 0xFF);
    
    /* 设置行地址 */
    ST7735_WriteCommand(0x2B);
    ST7735_WriteData(y0 >> 8);
    ST7735_WriteData(y0 & 0xFF);
    ST7735_WriteData(y1 >> 8);
    ST7735_WriteData(y1 & 0xFF);
    
    /* 开始写入数据 */
    ST7735_WriteCommand(0x2C);
}

