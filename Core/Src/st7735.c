/**
 * @brief  显示导航界面（全屏：仅汉字指路 + 底部车牌号）
 * @param  i,j: 目标车位坐标（与路径规划一致）
 * @param  license_plate: 车牌号 ASCII，可为 NULL 显示为 --
 */
static void st7735_nav_hz_put(uint16_t x, uint16_t y, const uint8_t *idx, uint8_t n, uint16_t fg, uint16_t bg)
{
    uint8_t k;
    for (k = 0U; k < n; k++) {
        ST7735_DrawChinese((uint16_t)(x + (uint16_t)k * 16U), y, idx[k], fg, bg);
    }
}

static void st7735_nav_hz_center_y(uint16_t y, const uint8_t *idx, uint8_t n, uint16_t fg, uint16_t bg)
{
    uint16_t w = (uint16_t)n * 16U;
    uint16_t x = 0U;
    if (w < ST7735_WIDTH) {
        x = (ST7735_WIDTH - w) / 2U;
    }
    st7735_nav_hz_put(x, y, idx, n, fg, bg);
}

static void st7735_nav_draw_plate(char *license_plate)
{
    const uint8_t chepai[] = {0U, 25U};
    char disp[20];
    uint16_t x;
    size_t ln;
    uint16_t w;

    if (license_plate != NULL && license_plate[0] != '\0') {
        strncpy(disp, license_plate, sizeof(disp) - 1U);
        disp[sizeof(disp) - 1U] = '\0';
    } else {
        disp[0] = '-';
        disp[1] = '-';
        disp[2] = '\0';
    }
    ln = strlen(disp);
    w = 32U + 8U + (uint16_t)ln * 8U;
    x = (w < ST7735_WIDTH) ? (ST7735_WIDTH - w) / 2U : 0U;
    st7735_nav_hz_put(x, ST7735_NAV_PLATE_Y, chepai, 2U, ST7735_COLOR_YELLOW, ST7735_COLOR_BLACK);
    x += 32U;
    ST7735_DrawString(x, ST7735_NAV_PLATE_Y, ":", ST7735_COLOR_YELLOW, ST7735_COLOR_BLACK);
    x += 8U;
    ST7735_DrawString(x, ST7735_NAV_PLATE_Y, disp, ST7735_COLOR_CYAN, ST7735_COLOR_BLACK);
}

