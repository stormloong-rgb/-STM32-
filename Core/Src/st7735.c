/**
 * @brief  绘制单个像素点
 * @param  x, y: 像素坐标
 * @param  color: RGB565颜色值
 */
void ST7735_DrawPixel(uint16_t x, uint16_t y, uint16_t color)
{
    uint8_t data[2];
    data[0] = color >> 8;
    data[1] = color & 0xFF;
    
    ST7735_SetAddrWindow(x, y, x, y);
    HAL_GPIO_WritePin(ST7735_DC_PORT, ST7735_DC_PIN, GPIO_PIN_SET);
    HAL_GPIO_WritePin(ST7735_CS_PORT, ST7735_CS_PIN, GPIO_PIN_RESET);
    HAL_SPI_Transmit(&hspi2, data, 2, 100);
    HAL_GPIO_WritePin(ST7735_CS_PORT, ST7735_CS_PIN, GPIO_PIN_SET);
}

/**
 * @brief  绘制单个ASCII字符
 * @param  x, y: 起始坐标
 * @param  c: 字符（支持可打印ASCII字符）
 * @param  fg_color: 前景色（字符颜色）
 * @param  bg_color: 背景色
 */
void ST7735_DrawChar(uint16_t x, uint16_t y, char c, uint16_t fg_color, uint16_t bg_color)
{
    uint8_t i, j;
    uint8_t line;
    uint8_t k;
    
    /* 边界检查 */
    if (c < ' ') c = ' ';
    if (c > '~') c = '~';
    k = c - 32;
    
    /* 16行，每行8像素 */
    for (i = 0; i < 16; i++)
    {
        line = asc16[k * 16 + i];
        
        for (j = 0; j < 8; j++)
        {
            /* 从高位到低位检查，0x80 >> j */
            if (line & (0x80 >> j))
            {
                ST7735_DrawPixel(x + j, y + i, fg_color);
            }
            else if (fg_color != bg_color)
            {
                ST7735_DrawPixel(x + j, y + i, bg_color);
            }
        }
    }
}

void ST7735_DrawChinese(uint16_t x, uint16_t y, uint8_t index, uint16_t fg_color, uint16_t bg_color)
{
    uint8_t row, col;
    uint16_t bits;

    if (index >= ST7735_HZ_COUNT) {
        return;
    }
    const uint8_t *g = st7735_hz_font[index];
    for (row = 0U; row < 16U; row++) {
        bits = ((uint16_t)g[row * 2U] << 8) | (uint16_t)g[row * 2U + 1U];
        for (col = 0U; col < 16U; col++) {
            if (bits & (uint16_t)(0x8000U >> col)) {
                ST7735_DrawPixel((uint16_t)(x + col), (uint16_t)(y + row), fg_color);
            } else if (fg_color != bg_color) {
                ST7735_DrawPixel((uint16_t)(x + col), (uint16_t)(y + row), bg_color);
            }
        }
    }
}

/**
 * @brief  绘制字符串
 * @param  x, y: 起始坐标
 * @param  str: 字符串指针
 * @param  fg_color: 前景色
 * @param  bg_color: 背景色
 * @note   每个字符宽度8像素
 */
void ST7735_DrawString(uint16_t x, uint16_t y, char *str, uint16_t fg_color, uint16_t bg_color)
{
    uint16_t i = 0;
    
    while (str[i] != '\0')
    {
        ST7735_DrawChar(x + i * 8, y, str[i], fg_color, bg_color);
        i++;
    }
}

